package com.s8.io.bohr.neon.generators;

public class NeGeneratedResource {

	
	/**
	 * To be re-interpreted as a MIME type
	 */
	public final String type;
	
	
	/**
	 * data
	 */
	public final byte[] data;

	
	/**
	 * 
	 * @param type
	 * @param data
	 */
	public NeGeneratedResource(String type, byte[] data) {
		super();
		this.type = type;
		this.data = data;
	}
	
	
}
